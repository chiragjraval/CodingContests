import java.util.Scanner;

public class PatternProblem1 {

	public static void main(String[] args) {
		Scanner inScanner = new Scanner(System.in);
		
		int N = inScanner.nextInt();
		for(int i=0; i<N; i++) {
			System.out.println("1 2 3 4 5");
		}
		
		inScanner.close();
	}
	
}
